public class Student {
    int rollNo;
    String name;
    String course;
    double marks;

    void displayDetails() {
        System.out.println("---Student Details---");
        System.out.println("Roll No: " + rollNo);
        System.out.println("Name: " + name);
        System.out.println("Course: " + course);
        System.out.println("Marks: " + marks);
    }
}
