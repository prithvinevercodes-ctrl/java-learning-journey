package com.example;

// This class only prints common Maven commands for practice
public class MavenCommandPractice {
    public static void main(String[] args) {
        System.out.println("Common Maven Commands:");
        System.out.println("1. mvn compile        -> Compile source code");
        System.out.println("2. mvn test           -> Run test cases");
        System.out.println("3. mvn package        -> Create JAR/WAR file");
        System.out.println("4. mvn clean          -> Delete target folder");
        System.out.println("5. mvn clean install  -> Clean, build and install project");
        System.out.println("6. mvn dependency:tree -> Show dependency tree");
    }
}
