public class EvenOddChecker {
    public static void main(String[] args) {
        int number = 17;

        System.out.println("Even or Odd Checker");
        if (number % 2 == 0) {
            System.out.println(number + " is Even");
        } else {
            System.out.println(number + " is Odd");
        }
    }
}
